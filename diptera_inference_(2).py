import gradio as gr

# Build the interface
with gr.Blocks(title='Diptera Larvae Identification') as app:

    gr.Markdown("""
    # Diptera Larvae Identification
    **IHMT / IPS Bioinformatics Internship**

    Upload microscopic images of a mosquito larva.
    You can upload one or more structures — the more you provide, the more confident the result.

    **Image naming:** files should contain the structure name (cab, seg8, segan, sif).
    Do not upload scale-bar versions (-sc).

    The result shows a percentage for each candidate genus/species, plus a single
    **"Unknown"** slice — all summing to 100%. The Unknown share reflects how far
    the image is from anything seen during training (not just softmax confidence):
    a larva similar to the training set should show a small Unknown share, while
    one from a species outside the whole dataset should show a large one.
    """)

    with gr.Row():
        cab_input   = gr.Image(type='pil', label='Cephalic capsule (cab)')
        seg8_input  = gr.Image(type='pil', label='Segment 8 (seg8)')
        segan_input = gr.Image(type='pil', label='Anal segment (segAn)')
        sif_input   = gr.Image(type='pil', label='Siphon (sif) — not present in Anopheles')

    run_btn = gr.Button('Identify', variant='primary')

    with gr.Row():
        genus_out   = gr.Textbox(label='Level 1 — Genus', lines=12)
        species_out = gr.Textbox(label='Level 2 — Species', lines=12)

    run_btn.click(
        fn=identify_larva,
        inputs=[cab_input, seg8_input, segan_input, sif_input],
        outputs=[genus_out, species_out]
    )

